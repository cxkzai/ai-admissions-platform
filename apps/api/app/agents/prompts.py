# ===== Prompt 字典 =====

AGENT_PROMPTS: dict[str, str] = {
    "admission-consultant": ADMISSION_CONSULTANT_PROMPT,
    "course-consultant": COURSE_CONSULTANT_PROMPT,
    "academic-teacher": ACADEMIC_TEACHER_PROMPT,
    "internal-assistant": INTERNAL_ASSISTANT_PROMPT,
}


def get_agent_prompt(slug: str) -> str:
    """获取 Agent 的 System Prompt."""
    return AGENT_PROMPTS.get(slug, ADMISSION_CONSULTANT_PROMPT)